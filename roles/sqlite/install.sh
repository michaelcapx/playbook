#! /usr/bin/env bash
set -e
###########################################################################
#
# SQLite Bootstrap Installer
# https://github.com/polymimetic/playbook/roles/sqlite
#
# This script is intended to replicate the ansible role in a shell script
# format. It can be useful for debugging purposes or as a quick installer
# when it is inconvenient or impractical to run the ansible playbook.
#
# Usage:
# wget -qO - https://raw.githubusercontent.com/polymimetic/playbook/master/roles/sqlite/install.sh | bash
#
###########################################################################

if [ `id -u` = 0 ]; then
  printf "\033[1;31mThis script must NOT be run as root\033[0m\n" 1>&2
  exit 1
fi

###########################################################################
# Constants and Global Variables
###########################################################################

readonly GIT_REPO="https://github.com/polymimetic/playbook.git"
readonly GIT_RAW="https://raw.githubusercontent.com/polymimetic/playbook/master/roles/sqlite"

###########################################################################
# Basic Functions
###########################################################################

# Output Echoes
# https://github.com/cowboy/dotfiles
function e_error()   { echo -e "\033[1;31m✖  $@\033[0m";     }      # red
function e_success() { echo -e "\033[1;32m✔  $@\033[0m";     }      # green
function e_prompt()  { echo -e "\033[1;33m$@ \033[0m";       }      # yellow
function e_info()    { echo -e "\033[1;34m$@\033[0m";        }      # blue
function e_title()   { echo -e "\033[1;35m$@.......\033[0m"; }      # magenta

###########################################################################
# Install SQLite
# https://www.sqlite.org/
#
# https://linuxhint.com/install-sqlite-ubuntu-linux-mint/
###########################################################################

install_sqlite() {
  e_title "Installing SQLite"

  # Install SQLite
  sudo apt-get install -yq sqlite3 libsqlite3-dev

  e_success "SQLite installed"
}

###########################################################################
# Program Start
###########################################################################

program_start() {
  install_sqlite
}

program_start
